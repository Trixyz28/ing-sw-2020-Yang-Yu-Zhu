package it.polimi.ingsw.messages;

/**
 * Class Tags used in the communication between clients-server to distinguish the different types of <code>Obj</code> objects.
 * @author GC44
 * @version 1.0
 * @since 1.0
 */

public class Tags {


    /**
     *Tag for a generic obj.
     */
    public static final String GENERIC = "generic";

    /**
     *Tag for a nameMsg obj.
     */
    public static final String NAME_MSG = "nameMsg";

    /**
     *Tag for a setName obj.
     */
    public static final String SET_NAME = "setName";

    /**
     *Tag for a lobbyOk obj.
     */
    public static final String LOBBY_OK = "lobbyOk";

    /**
     *Tag for a playerList obj.
     */
    public static final String PLAYER_LIST = "playerList";

    /**
     *Tag for a defineGod obj.
     */
    public static final String DEFINE_GOD = "defineGod";

    /**
     *Tag for a chooseGod obj.
     */
    public static final String CHOOSE_GOD = "chooseGod";

    /**
     *Tag for a completeList obj.
     */
    public static final String COMPLETE_LIST = "completeList";

    /**
     *Tag for a currentList obj.
     */
    public static final String CURRENT_LIST = "currentList";

    /**
     *Tag for a turn obj.
     */
    public static final String TURN = "turn";

    /**
     *Tag for a godMsg obj.
     */
    public static final String GOD_MSG = "godMsg";

    /**
     *Tag for a operation obj.
     */
    public static final String OPERATION = "operation";

    /**
     *Tag for a board obj.
     */
    public static final String BOARD = "board";

    /**
     *Tag for a boardMsg obj.
     */
    public static final String BOARD_MSG = "boardMsg";

    /**
     *Tag for a gMsg obj.
     */
    public static final String G_MSG = "gMsg";

    /**
     *Tag for an end obj.
     */
    public static final String END = "end";

    /**
     *Tag for match initialization.
     */
    public static final String SETUP = "setup";

}
